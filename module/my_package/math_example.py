# math 모듈 - 수학 함수
import math

print(f"Circumference(π): {math.pi}")
print(f"natural constant(e): {math.e}")
print(f"squareRoot of 9: {math.sqrt(9)}")
print(f"ceilingValue ceil(3.2): {math.ceil(3.2)}")
print(f"floorValue floor(3.8): {math.floor(3.8)}")
print(f"5 to the power of 3: {math.pow(5, 3)}")
print(f"60도의 사인값: {math.sin(math.radians(60))}")